#ifndef LLVM_LIB_TARGET_Glulx_GlulxSUBTARGET_H
#define LLVM_LIB_TARGET_Glulx_GlulxSUBTARGET_H

#include "GlulxFrameLowering.h"
#include "GlulxISelLowering.h"
#include "GlulxInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/MC/MCInstrItineraries.h"

#define GET_SUBTARGETINFO_HEADER
#include "GlulxGenSubtargetInfo.inc"

namespace llvm {
class GlulxSubtarget : public GlulxGenSubtargetInfo {
protected:
  SelectionDAGTargetInfo TSInfo;
  GlulxInstrInfo InstrInfo;
  GlulxFrameLowering FrameLowering;
  GlulxTargetLowering TLInfo;
  GlulxRegisterInfo RegInfo;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  GlulxSubtarget(const Triple &TT, StringRef CPU, StringRef TuneCPU,
                 StringRef FS, const TargetMachine &TM);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  GlulxSubtarget &initializeSubtargetDependencies(const Triple &TT,
                                                  StringRef CPU,
                                                  StringRef TuneCPU,
                                                  StringRef FS,
                                                  const TargetMachine &TM);

  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  const GlulxInstrInfo *getInstrInfo() const override {
    return &InstrInfo;
  }

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const GlulxRegisterInfo *getRegisterInfo() const override {
    return &RegInfo;
  }

  const GlulxTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }

  bool enableMachineScheduler() const override { return false; }

  /// getMaxInlineSizeThreshold - Returns the maximum memset / memcpy size
  /// that still makes it profitable to inline the call.
  unsigned getMaxInlineSizeThreshold() const {
    return 64;
  }
};
}

#endif // end LLVM_LIB_TARGET_Glulx_GlulxSUBTARGET_H
